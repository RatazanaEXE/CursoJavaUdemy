package src.asecaoAnterioresa14;

public class ContagemDeDoces {
    public static void main(String[] args) {
        int mae = 3;
        int filho = 0;

        while (filho <= mae) {
            System.out.println("Filho comeu "+filho+ " doces.");
            filho++;
        }
        System.out.println("Não pode comer mais doces.");

    }
}
